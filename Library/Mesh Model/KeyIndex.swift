import Foundation

/// The key index is a 12-bit unsigned integer identifying a Network
/// or an Application Key.
///
/// The key indexes within Network Keys and Application Keys must
/// be distinct.
///
/// This type is an alias for `UInt16`. To check the range, use
/// `isValidKeyIndex`.
public typealias KeyIndex = UInt16

public extension KeyIndex {
    
    /// A Key Index is 12-bit long Unsigned Integer.
    /// This property returns `true` if the value is in range 0...4095.
    var isValidKeyIndex: Bool {
        return self >= 0 && self <= 4095
    }
    
}
